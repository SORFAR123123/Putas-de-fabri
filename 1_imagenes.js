function mostrarInfoContenedor(modo, numero) {
    if (!sistemaDescriptivo[modo]) {
        console.error(`Modo ${modo} no existe`);
        return;
    }
    
    const contenedor = sistemaDescriptivo[modo].contenedores[numero];
    if (!contenedor) {
        console.error(`Contenedor ${numero} no existe en modo ${modo}`);
        return;
    }
    
    console.log(`📦 Contenedor ${numero} (${modo}):`);
    console.log(`   Nombre: ${contenedor.nombre}`);
    console.log(`   Imagen: ${contenedor.imagen || '(sin imagen)'}`);
    console.log(`   Descripción: ${contenedor.descripcion}`);
    console.log(`   Tipo/Categoría: ${contenedor.tipo || contenedor.categoria || contenedor.genero || contenedor.ambiente || contenedor.tematica || 'N/A'}`);
}

function mostrarInfoSubcontenedor(modo, contenedor, subcontenedor) {
    if (!sistemaDescriptivo[modo]) {
        console.error(`Modo ${modo} no existe`);
        return;
    }
    
    const key = `${contenedor}_${subcontenedor}`;
    const sub = sistemaDescriptivo[modo].subcontenedores[key];
    
    if (!sub) {
        console.error(`Subcontenedor ${key} no existe en modo ${modo}`);
        return;
    }
    
    console.log(`📦 Subcontenedor ${contenedor}_${subcontenedor} (${modo}):`);
    console.log(`   Nombre: ${sub.nombre}`);
    console.log(`   Imagen: ${sub.imagen || '(sin imagen)'}`);
    console.log(`   Descripción: ${sub.descripcion || '(sin descripción)'}`);
    
    // Mostrar campo específico según el modo
    const campoEspecifico = {
        'mangas': 'nivel',
        'animes': 'episodios',
        'audios': 'artista',
        'asmr': 'tipoVoz',
        'videos': 'duracion'
    }[modo];
    
    if (campoEspecifico && sub[campoEspecifico]) {
        console.log(`   ${campoEspecifico}: ${sub[campoEspecifico]}`);
    }
}

