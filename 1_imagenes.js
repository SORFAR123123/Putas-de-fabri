// EDITAR CONTENEDOR
function editarContenedor(modo, numero, nuevosDatos) {
    if (!sistemaDescriptivo[modo]) {
        console.error(`Modo ${modo} no existe`);
        return false;
    }
    
    if (!sistemaDescriptivo[modo].contenedores[numero]) {
        console.error(`Contenedor ${numero} no existe en modo ${modo}`);
        return false;
    }
    
    sistemaDescriptivo[modo].contenedores[numero] = {
        ...sistemaDescriptivo[modo].contenedores[numero],
        ...nuevosDatos
    };
    
    console.log(`✅ Contenedor ${numero} actualizado en modo ${modo}:`, nuevosDatos);
    return true;
}

